function getRandomInt(min, max, isFloat) {
    min = Math.ceil(min);
    max = Math.floor(max);
    return isFloat ? Math.random() * (max - min) + min : Math.floor(Math.random() * (max - min) + min); //The maximum is exclusive and the minimum is inclusive
}

function getStayRate(reviews) {
    const ratePoints = reviews.reduce((acc, { rate }) => {
        for (const key in rate) {
            acc[key] += rate[key]
        }
        return acc
    }, {
        cleanliness: 0,
        communication: 0,
        checkin: 0,
        accuracy: 0,
        value: 0,
        location: 0,
    })

    const rateKeys = Object.keys(ratePoints)
    const rateSum = rateKeys.reduce((acc, key) => {
        acc += ratePoints[key]
        return acc
    }, 0)
    return +(rateSum / (reviews.length * rateKeys.length)).toFixed(1)
}

